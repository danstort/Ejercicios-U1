window.onload = function () {

    let palabra = prompt("Introduce la palabra que quieres traducir:")

    switch (palabra) {

        case "mesa":

            alert("table")
            break;

        case "casa":

            alert("house")
            break;

        case "perro":

            alert("dog")
            break;

        case "gato":

            alert("cat")
            break;

    }

}